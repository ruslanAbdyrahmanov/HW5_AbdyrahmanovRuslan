public class Boss {

    private int damage;

    private int health;
    private String defens;


    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public String getDefens() {
        return defens;
    }

    public void setDefens(String defens) {
        this.defens = defens;
    }
}
